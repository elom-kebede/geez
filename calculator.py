num1=int(input("enter the first number: "))
num2=int(input("enter the second number: "))
def add(num1,num2):
    return num1+num2
def sub(num1,num2):
    return num1-num2
def mul(num1,num2):
    return num1*num2
def div(num1,num2):
    return num1/num2
print(f"the sum is {add(num1,num2)}")
print(f"the sub is {sub(num1,num2)}")
print(f"the mul is {mul(num1,num2)}")
print(f"the division is {div(num1,num2)}")
